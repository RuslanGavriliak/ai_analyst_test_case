CUBE_TOTAL = [
    "sku_id = 'Total'",
    "territorial_group = 'Total'",
    "store_type = 'Total'",
    "purchase_type = 'Total'",
]


def cube(*extra: str) -> list[str]:
    return CUBE_TOTAL + list(extra)


def row(
    id_: str,
    question: str,
    *,
    difficulty: str,
    tags: list[str],
    family: str | None = None,
    domain_id: str | None = None,
    metric_ids: list[str] | None = None,
    sources: list[str] | None = None,
    entity_ids: list[str] | None = None,
    required_filters: list[str] | None = None,
    state: str = "ready_for_sql",
    reason_codes: list[str] | None = None,
) -> tuple[dict, dict]:
    task = {"id": id_, "question": question, "difficulty": difficulty, "tags": tags}
    if family:
        task["family"] = family
    codes = reason_codes
    if codes is None and state == "ready_for_sql":
        codes = ["good_context_match"]
    gold = {
        "id": id_,
        "domain_id": domain_id,
        "metric_ids": metric_ids or [],
        "sources": sources or [],
        "entity_ids": entity_ids or [],
        "required_filters": required_filters or [],
        "state": state,
        "reason_codes": codes or [],
    }
    return task, gold


